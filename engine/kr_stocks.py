def resolve_ticker(raw: str) -> str:
    """
    한글 입력이면 KR_TICKER_MAP 조회 후 {code}.KS 반환.
    영문 입력이면 .upper() 후 그대로 반환.
    """
    cleaned = raw.strip()
    if not cleaned:
        return cleaned

    # 한글 포함 여부 체크
    if any("\uAC00" <= c <= "\uD7A3" for c in cleaned):
        key = cleaned.lower().replace(" ", "")
        # 공백 제거 후 재시도
        for map_key, code in KR_TICKER_MAP.items():
            if map_key.replace(" ", "") == key:
                return f"{code}.KS"
        return cleaned  # 못 찾으면 원본 반환

    return cleaned.upper()


def search_kr_companies(query: str) -> list[dict]:
    """한글 쿼리로 유사 종목 목록 반환 (자동완성용)."""
    query = query.lower().replace(" ", "")
    results = []
    for name, code in KR_TICKER_MAP.items():
        if query in name.replace(" ", ""):
            results.append({"name": name, "ticker": f"{code}.KS"})
    return results[:10]
